returnTemp DW ?
b_1 DW ?
.CODE
MAIN PROC
;init ds
mov ax, @DATA
mov ds, ax

MOV AH, 4CH
INT 21H
MAIN ENDP
PROC print
MOV BX, 10
CMP AX, 0
JGE NOTNEGATIVE
NEG AX
MOV CX, AX
MOV DL, '-'
MOV AH, 2
INT 21H
MOV AX, CX
NOTNEGATIVE:
MOV CX, 0
CMP AX, 0
JNE PUSHREM
MOV AH, 2
MOV DL, '0'
INT 21H
JMP PRINTEND
PUSHREM:
MOV DX, 0
DIV BX
PUSH DX
INC CX
CMP AX, 0
JNE PUSHREM
PRINTREM:
POP DX
ADD DL, '0'
MOV AH, 2
INT 21H
LOOP PRINTREM
PRINTEND:
MOV dl, 10
MOV ah, 02h
INT 21h
MOV dl, 13
MOV ah, 02h
INT 21h
ret
PRINT ENDP
